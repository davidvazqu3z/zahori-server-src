/**
 * The type Job file parameter.
 */
public class JobFileParameter {

    private String name;
    private String filePath;
    private String fileName;

    /**
     * Instantiates a new Job file parameter.
     *
     * @param name     the name
     * @param filePath the file path
     * @param fileName the file name
     */
    public JobFileParameter(String name, String filePath, String fileName) {
        super();
        this.name = name;
        this.filePath = filePath;
        this.fileName = fileName;
    }

    @Override
    public String toString() {
        return "JobFileParameter [name=" + name + ", filePath=" + filePath + ", fileName=" + fileName + "]";
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets file path.
     *
     * @return the file path
     */
    public String getFilePath() {
        return filePath;
    }

    /**
     * Sets file path.
     *
     * @param filePath the file path
     */
    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    /**
     * Gets file name.
     *
     * @return the file name
     */
    public String getFileName() {
        return fileName;
    }

    /**
     * Sets file name.
     *
     * @param fileName the file name
     */
    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

}
